# Metric Converter: Meters to Centimeters and Millimeters

# Ask the user for a value in meters
meters = float(input("Enter the value in meters: "))

# Convert meters to centimeters and millimeters
centimeters = meters * 100
millimeters = meters * 1000

# Display the results in a clean format
print(f"{meters} meters are equal to {centimeters} centimeters and {millimeters} millimeters.")
